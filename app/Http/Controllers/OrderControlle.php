     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $order = Order::find($id);
        $order->status=$request->status;
        $order->save();
        return redirect()->route('orders.index')
                        ->with('success','Order update successfully');
        // dd($request->all());
